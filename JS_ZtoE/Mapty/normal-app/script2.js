//Architecture. -> create App Class
class App {
  //private instance properties
  #map;
  #mapZoomLevel = 13;
  #mapEvent;
  #workouts = [];

  constructor() {
    // Get user's position
    this._getPosition();

    // Get data from local storage
    this._getLocalStorage();
    // Attach event handler
    form.addEventListener('submit', this._newWorkout.bind(this));
    inputType.addEventListener('change', this._toggleElevationField.bind(this));
    containerWorkouts.addEventListener('click', this._moveToPopup.bind(this));
  }

  _getPosition() {
    navigator.geolocation?.getCurrentPosition(this._loadMap.bind(this), () => {
      alert('Could not get your position T^T');
    });
  }
  _loadMap(position) {
    const { latitude, longitude } = position.coords;
    const coords = [latitude, longitude];
    this.#map = L.map('map').setView(coords, this.#mapZoomLevel);
    L.tileLayer('https://{s}.tile.openstreetmap.fr/hot/{z}/{x}/{y}.png', {
      attribution: '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors',
    }).addTo(this.#map);

    this.#map.on('click', this._showForm.bind(this));
    this.#workouts.forEach(el => this._renderWorkoutMarker(el));
  }
  _showForm(e) {
    this.#mapEvent = e;
    form.classList.remove('hidden');
    inputDistance.focus();
  }
  _hideForm() {
    inputDistance.value = inputDuration.value = inputCadence.value = inputElevation.value = '';
    form.style.display = 'none';
    // Ìèº hidden Ïä§ÌÉÄÏùºÏóê Ïï†ÎãàÎ©îÏù¥ÏÖòÏù¥ Îì§Ïñ¥Í∞ÄÏûàÏùå. ÌèºÏù¥ ÎÇòÌÉÄÎÇ†Îïå Ìö®Í≥ºÏ£ºÎ†§Í≥† ÏÇΩÏûÖÌïúÍ±¥Îç∞, ÏÇ≠Ï†úÎê†ÎïåÎèÑ Ï†ÅÏö©Îê®. ÎÇòÌÉÄÎÇ†ÎïåÎßå Ïä§ÌÉÄÏùºÏùÑ Ï†ÅÏö©ÌïòÎäî Î≤ïÏùÄ CssÏóê ÏóÜÏùå.. Îã§Î•∏ Î∞©Î≤ïÎèÑ ÏûàÍ≤†ÏßÄÎßå Ïù¥Î†áÍ≤å Í∑∏ÎÉ• ÏûëÏùÄ Ìä∏Î¶≠ÏúºÎ°ú Ï≤òÎ¶¨Ìï¥ÎèÑ ÎêúÎã§.
    form.classList.add('hidden');
    setTimeout(() => {
      form.style.display = 'grid';
    }, 1000);
  }
  _toggleElevationField() {
    inputElevation.closest('.form__row').classList.toggle('form__row--hidden');
    inputCadence.closest('.form__row').classList.toggle('form__row--hidden');
  }
  _newWorkout(e) {
    e.preventDefault();

    // -get data from form
    const { lat, lng } = this.#mapEvent.latlng;
    const type = inputType.value;
    const distance = +inputDistance.value;
    const duration = +inputDistance.value;

    // -if activity running -> create running object
    const selectOption = type === 'running' ? +inputCadence.value : +inputElevation.value;

    // -check if data is valid
    if (Number.isNaN(distance) || Number.isNaN(duration) || (Number.isNaN(selectOption) && distance > 0 && duration > 0 && selectOption > 0))
      return alert('Inputs have to be positive numbers!');

    // -add new object to workout array
    const inputData = [[lat, lng], distance, duration, selectOption];
    const workout = type === 'running' ? new Running(...inputData) : new Cycling(...inputData);
    this.#workouts.push(workout);

    // -render workout on map as marker and on list.
    this._renderWorkoutMarker(workout);
    this._renderWorkout(workout);

    // -hide form and clear input fields.
    this._hideForm();

    //Set local storage to all workouts
    this._setLocalStorage();
  }
  _renderWorkoutMarker(workout) {
    L.marker(workout.coords)
      .addTo(this.#map)
      .bindPopup(
        L.popup({
          maxWidth: 250,
          minWidth: 100,
          autoClose: false,
          closeOnClick: false,
          // className: type === 'running' ? 'running-popup' : 'cycling-popup',
          className: `${workout.type}-popup`,
        })
      )
      .setPopupContent(`${workout.type === 'running' ? 'üèÉ‚Äç‚ôÇÔ∏è' : 'üö¥‚Äç‚ôÄÔ∏è'} ${workout.description}`)
      .openPopup();
  }
  _renderWorkout(workout) {
    let html = `
      <li class="workout workout--${workout.type}" data-id="${workout.id}">
        <h2 class="workout__title">${workout.description}</h2>
        <div class="workout__details">
          <span class="workout__icon">${workout.type === 'running' ? 'üèÉ‚Äç‚ôÇÔ∏è' : 'üö¥‚Äç‚ôÄÔ∏è'}</span>
          <span class="workout__value">${workout.distance}</span>
          <span class="workout__unit">km</span>
        </div>
        <div class="workout__details">
          <span class="workout__icon">‚è±</span>
          <span class="workout__value">${workout.duration}</span>
          <span class="workout__unit">min</span>
        </div>
      `;
    if (workout.type === 'running')
      html += `          
          <div class="workout__details">
            <span class="workout__icon">‚ö°Ô∏è</span>
            <span class="workout__value">${workout.pace.toFixed(1)}</span>
            <span class="workout__unit">min/km</span>
          </div>
          <div class="workout__details">
            <span class="workout__icon">ü¶∂üèº</span>
            <span class="workout__value">${workout.cadence}</span>
            <span class="workout__unit">spm</span>
          </div>
        </li>
        `;
    if (workout.type === 'cycling')
      html += `          
            <div class="workout__details">
              <span class="workout__icon">‚ö°Ô∏è</span>
              <span class="workout__value">${workout.speed.toFixed(1)}</span>
              <span class="workout__unit">km/h</span>
            </div>
            <div class="workout__details">
              <span class="workout__icon">‚õ∞</span>
              <span class="workout__value">${workout.elevationGain}</span>
              <span class="workout__unit">m</span>
            </div>
          </li>
          `;
    form.insertAdjacentHTML('afterend', html);
  }
  _moveToPopup(e) {
    const workoutEl = e.target.closest('.workout');
    if (!workoutEl) return;
    const workout = this.#workouts.find(el => el.id === workoutEl.dataset.id);
    this.#map.setView(workout.coords, this.#mapZoomLevel, { animate: true, pan: { duration: 1 } });

    //using the public interface
    workout.click();
  }
  _setLocalStorage() {
    localStorage.setItem('workouts', JSON.stringify(this.#workouts));
  }
  _getLocalStorage() {
    const data = localStorage.getItem('workouts');
    if (!data) return;
    this.#workouts = JSON.parse(data);

    //render workouts
    this.#workouts.forEach(el => this._renderWorkout(el));
  }
  // public methods. use in console.
  reset() {
    localStorage.removeItem('workouts');
    location.reload();
  }
}

